// 1.string()
// 2.Number()
// 3.Boolean()

// String conversion
let a = 5;
document.write(a + "<br>");
document.write(typeof a + "<br>");
let b = String(a);
document.write(b + "<br>");
document.write(typeof b + "<br>");
document.write("<br>");

let c = true;
document.write(c + "<br>");
document.write(typeof c + "<br>");
let d = String(c);
document.write(d + "<br>");
document.write(typeof d + "<hr>");

// Number conversion
let e = "5";
document.write(e + "<br>");
document.write(typeof e + "<br>");
let f = Number(e);
document.write(f + 10 + "<br>");
document.write(typeof f + "<br>");
document.write("<br>");

let g = false;
document.write(g + "<br>");
document.write(typeof g + "<br>");
let h = Number(g);
document.write(h + "<br>");
document.write(typeof h + "<hr>");

// Boolean conversion
let i = "10";
document.write(i + "<br>");
document.write(typeof i + "<br>");
let j = Boolean(i);
document.write(j + "<br>");
document.write(typeof j + "<br>");
document.write("<br>");

let k = 0;
document.write(k + "<br>");
document.write(typeof k + "<br>");
let l = Boolean(k);
document.write(l + "<br>");
document.write(typeof l + "<hr>");




