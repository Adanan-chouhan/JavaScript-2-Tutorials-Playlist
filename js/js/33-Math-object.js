// Math object
console.log(Math.PI);

console.log(Math.E);

// round(); .6 after so upper value and .5 before lower value 
let a = Math.round(4.2);
console.log(a);

// ceil(); .after so upper value 
let b = Math.ceil(5.2);
console.log(b);

// floor(); .after so lower value
let c = Math.floor(3.2);
console.log(c);

// trunc(); .after so not value output 
let d = Math.trunc(2.2);
console.log(d);

// pow(); 
let e = Math.pow(2,3);
console.log(e);

// sqrt(); 
let f = Math.sqrt(100);
console.log(f);

//min();
let g = Math.min(10,20,3,425,18,8);
console.log(g);


//max();
let h = Math.max(10,20,3,425,18,8);
console.log(h);

